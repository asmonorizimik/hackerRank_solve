d1 = {'a': 100, 'b': 200, 'c':300}
d2 = {'a': 300, 'b': 200, 'd':400}
a=list(d1)#["a","b","c"]
b=list(d2)##["a","b","d"]
x={}
i=0
while i<len(a):#0<3:
    if a[i] == b[i]:##a == a
        x[a[i]]=d1[a[i]]+d2[b[i]]##{"a"=100+300}
    else:
        x[a[i]]=d1[a[i]]#{"c"="c"=300}
        x[b[i]]=d2[b[i]]#{"d"="d"=400}
    i=i+1
print(x)##{'a': 400, 'b': 400, 'c': 300, 'd': 400}